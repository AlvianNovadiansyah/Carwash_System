    private void displayData() {
      Connection con = Koneksi.koneksi();
      try {
         Statement stmt = con.createStatement();
         ResultSet rs = stmt.executeQuery("SELECT * FROM carwash_section");
         DefaultTableModel model = new DefaultTableModel();
         model.addColumn("Section ID");
         model.addColumn("Section Number");
         model.addColumn("Section Type");
         model.addColumn("Section Status");
         
         while (rs.next()) {
            String SecId = rs.getString("Section_ID");
            String SecNumber = rs.getString("Section_Number");
            String SecType = rs.getString("Section_Type");
            String SecStatus = rs.getString("Section_Status");
            
            model.addRow(new Object[]{SecId, SecNumber, SecType, SecStatus});
         }
         SectionTable.setModel(model);
         if(stmt != null){
             stmt.close();
         }
      } catch (SQLException e) {
         System.out.println(e);
      } finally {
          Koneksi.closeConnection();
      }
   }

