    private void SimpanSectionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanSectionActionPerformed
        // TODO add your handling code here:
         Connection con = Koneksi.koneksi();
        if (evt.getSource() == SimpanSection) {
            String id = SectionIdinput.getText();
            String number = SectionNuminput.getText();
            String type = SectionTypeinput.getSelectedItem().toString();
            String status = SectionStainput.getText();
            
             try{
                PreparedStatement st = con.prepareStatement("INSERT INTO carwash_section(Section_ID, Section_Number, "
                        + "Section_Type, Section_Status) "
                        + "VALUES(?, ?, ?, ?)");
                st.setString(1, id);
                st.setString(2, number);
                st.setString(3, type);
                st.setString(4, status);
                st.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "Data inserted successfully!", "Insert Success", JOptionPane.INFORMATION_MESSAGE); 
                if(st != null){
                   st.close(); 
                }
             } catch(SQLException ex){
                 JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage(), "Insert Error", JOptionPane.ERROR_MESSAGE);
             } finally {
                 Koneksi.closeConnection();
             }
             
             displayData();
            SectionIdinput.setText("");
            SectionNuminput.setText("");
            SectionTypeinput.setSelectedIndex(0);   
            SectionStainput.setText("");
        }
    }//GEN-LAST:event_SimpanSectionActionPerformed

    private void HapusSectionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusSectionActionPerformed
        // TODO add your handling code here:\
        Connection con = Koneksi.koneksi();
        try{
            PreparedStatement stmt = con.prepareStatement("DELETE FROM carwash_section WHERE Section_ID = ?");
            stmt.setString(1, SectionID);
            int rowDelete = stmt.executeUpdate();
            if(rowDelete > 0){
                JOptionPane.showMessageDialog(null, "Row deleted successfully.");
            } else {
            JOptionPane.showMessageDialog(null, "Row not found.");
            }
            if(stmt != null){
                stmt.close();
            }
        } catch(SQLException ex){
             JOptionPane.showMessageDialog(null, "Error deleting row: " + ex.getMessage());
        } finally{
            Koneksi.closeConnection();
        }
        
        displayData();
        SectionIdinput.setText("");
        SectionNuminput.setText("");
        SectionTypeinput.setSelectedIndex(0);   
        SectionStainput.setText("");
    }//GEN-LAST:event_HapusSectionActionPerformed

